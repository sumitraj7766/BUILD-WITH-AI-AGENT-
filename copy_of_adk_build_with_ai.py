"""## What is ReAcT and Planner for the Agent Execution

planner (Optional): Assign a BasePlanner instance to enable multi-step reasoning and planning before execution. There are two main planners:

- BuiltInPlanner: Leverages the model's built-in planning capabilities (e.g., Gemini's thinking feature).
- PlanReActPlanner: This planner instructs the model to follow a specific structure in its output: first create a plan, then execute actions (like calling tools), and provide reasoning for its steps.
"""

from google.adk.planners import PlanReActPlanner

APP_NAME = "compare"
USER_ID = "user1"
SESSION_ID = "session_plan"

session = asyncio.run(session_service.create_session(
    app_name=APP_NAME, user_id=USER_ID, session_id=SESSION_ID
))

search_agent_with_planning = LlmAgent(
    name="search_agent",
    model=model_name,
    description="Answers questions by searching the web in real time using Google Search.",
    instruction="You are a research assistant. Use Google Search to find accurate, up to date information and answer the user's question concisely.",
    tools=[google_search],
    planner=PlanReActPlanner()
)

def call_agent(query):
    runner = Runner(
          agent=search_agent_with_planning,
          app_name=APP_NAME,
          session_service=session_service
      )
    content = types.Content(role='user', parts=[types.Part(text=query)])
    events = runner.run(user_id=USER_ID, session_id=SESSION_ID, new_message=content)

    for event in events:
        print(f"\nDEBUG EVENT: {event}\n")
        if event.is_final_response() and event.content:
            final_answer = event.content.parts[0].text.strip()
            print("\n🟢 FINAL ANSWER\n", final_answer, "\n")

user_message3 = "Check if Nike, Adidas, and Puma have any ongoing discounts right now"

call_agent(query=user_message3)

