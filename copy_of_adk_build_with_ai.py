def check_inventory(product_name: str, current_stock: int, reorder_threshold: int) -> dict:
    """Checks current stock level against reorder threshold and returns stock status with reorder recommendation."""
    return {
        "product": product_name,
        "current_stock": current_stock,
        "needs_reorder": current_stock <= reorder_threshold,
        "status": "CRITICAL" if current_stock == 0 else "LOW" if current_stock <= reorder_threshold else "OK"
    }

inventory_agent = LlmAgent(
    model=model_name,
    name="inventory_agent",
    description="Monitors retail stock levels and alerts when products need reordering.",
    instruction="You are a retail inventory manager. Use the check_inventory tool to assess stock levels and give a clear reorder recommendation.",
    tools=[check_inventory]
)

run_agent(inventory_agent, """
  Check inventory for:
  Product: Nike Air Max
  Current stock: 12 units
  Reorder threshold: 20 units
""")

